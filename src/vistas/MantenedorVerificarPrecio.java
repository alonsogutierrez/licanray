    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       // TODO add your handling code here:
        ProductoDao productoDao = new ProductoDaoImpl();
        Producto producto = new Producto();

        if (!txtCodigo.getText().trim().equals("")) {
            String codigo = txtCodigo.getText().trim();

            producto = productoDao.buscarNombre(codigo);
            if (producto.getPrecioVenta() != 0) {
                lblNombre.setText(producto.getNombre());
                DecimalFormat formateador = new DecimalFormat("###,###");
                String texto = String.valueOf(producto.getPrecioVenta());
                String ff = formateador.format(Double.parseDouble(texto));

                lblPrecio.setText(String.valueOf(ff));
                lblPeso.setText("$");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Ingrese el codigo, por favor", "Ingrese codigo", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

