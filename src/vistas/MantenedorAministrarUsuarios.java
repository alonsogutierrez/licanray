    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        UsuarioDao usuarioDao = new UsuarioDaoImpl();
        if (!usuarioDao.usuarioExiste(txtNombreUsuario.getText().trim())) {
            if (txtClave.getText().trim().equals(txtConfirmarClave.getText().trim())) {
                if (txtNombreUsuario.getText().trim().equals("")) {
                    JOptionPane.showMessageDialog(null, "Escriba un nombre de usuario", "Error nombre", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (txtClave.getText().trim().equals("")) {
                    JOptionPane.showMessageDialog(null, "Escriba una clave de usuario", "Error clave", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (txtConfirmarClave.getText().trim().equals("")) {
                    JOptionPane.showMessageDialog(null, "Escriba la clave nuevamente", "Error clave", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (cbCargo.getSelectedItem().toString().equals("Seleccione cargo")) {
                    JOptionPane.showMessageDialog(null, "Seleccione el cargo del usuario", "Seleccione Cargo", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                Usuario usuario1 = new Usuario();
                usuario1.setIdUsuario(0);
                usuario1.setNombre(txtNombreUsuario.getText().trim());
                usuario1.setClave(txtConfirmarClave.getText().trim());
                usuario1.setCargo(cbCargo.getSelectedItem().toString());
                usuario1.setEstado(Byte.parseByte("1"));

                boolean resp = usuarioDao.ingresar(usuario1);
                if (!resp) {
                    JOptionPane.showMessageDialog(null, "Se ingreso el usuario correctamente");
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al ingresar el usuario", "Error de ingreso", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error, ya que las claves no son iguales", "Error en la clave", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error, ya que el nombre de usuario ya existe", "Error nombre usuario", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

