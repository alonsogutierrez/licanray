    private void btnBuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarUsuarioActionPerformed
        // TODO add your handling code here:
        UsuarioDao usuarioDao = new UsuarioDaoImpl();
        Usuario aux = new Usuario();

        if (!txtBuscarUsuario.getText().trim().equals("")) {
            aux = usuarioDao.buscar(txtBuscarUsuario.getText().trim());

            if (aux.getNombre() == null) {
                JOptionPane.showMessageDialog(null, "Error, ya que el nombre de usuario no existe", "Error nombre usuario", JOptionPane.ERROR_MESSAGE);
            } else {
                btnIngresar.setEnabled(false);
                btnActualizar.setEnabled(true);
                txtNombreUsuario.setEnabled(false);
                txtClave.setEnabled(false);
                txtConfirmarClave.setEnabled(false);
                cbCargo.setEnabled(false);
                txtNombreUsuario.setText(aux.getNombre());
                txtClave.setText(aux.getClave());
                txtConfirmarClave.setText(aux.getClave());
                cbCargo.setSelectedItem(aux.getCargo());
                cbxNombre.setEnabled(true);
                cbxClave.setEnabled(true);
                cbxCargo.setEnabled(true);
                if (aux.getEstado() == 1) {
                    lblEstado.setText("ACTIVO");
                } else {
                    lblEstado.setText("INACTIVO");
                }
                btnActivar.setEnabled(true);
                btnDesactivar.setEnabled(true);

            }
        }else{
            JOptionPane.showMessageDialog(this, "Ingrese un nombre de usuario, por favor."
                    ,"Error",JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnBuscarUsuarioActionPerformed

