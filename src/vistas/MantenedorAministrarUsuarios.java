    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        UsuarioDao usuarioDao = new UsuarioDaoImpl();
        if (txtClave.getText().trim().equals(txtConfirmarClave.getText().trim())) {
            if (cbCargo.getSelectedItem().toString().equals("Seleccione cargo")) {
                JOptionPane.showMessageDialog(null, "Seleccione un cargo, intentelo nuevamente.", "Error de cargo", JOptionPane.ERROR_MESSAGE);
                return;
            }
            usuarioDao.actualizar(txtBuscarUsuario.getText().trim(), txtNombreUsuario.getText().trim(), txtClave.getText().trim(), cbCargo.getSelectedItem().toString());
            limpiar();
            JOptionPane.showMessageDialog(null, "Se actualizo correctamente");
            btnActualizar.setEnabled(false);
            btnIngresar.setEnabled(true);
            btnActivar.setEnabled(false);
            btnDesactivar.setEnabled(false);
            cbxNombre.setEnabled(false);
            cbxClave.setEnabled(false);
            cbxCargo.setEnabled(false);
            txtNombreUsuario.setEnabled(true);
            txtClave.setEnabled(true);
            txtConfirmarClave.setEnabled(true);
            cbCargo.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Error, ya que las claves no son iguales", "Error clave", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

