    private void btnAplicarConfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAplicarConfActionPerformed
        // TODO add your handling code here:
        String real1, auxiliar1 = txtConfiguracionProd.getText().trim();
        real1 = auxiliar1.replace(".","");
        String real2, auxiliar2 = txtConfiguracionDin.getText().trim();
        real2 = auxiliar2.replace(".","");
        
        int cant_prod_nueva = Integer.parseInt(real1);
        int cant_din_nueva = Integer.parseInt(real2);
        
        //Verificamos que los valores nuevas de ambas variables sea mayor que cero.
        if(cant_prod_nueva >= 0 && cant_din_nueva > 0)
        {
            ConfiguracionDao config = new ConfiguracionDaoImpl();
            
            if(!cbxConfiguracionDin.isSelected() && !cbxConfiguracionProd.isSelected())
            {
                JOptionPane.showMessageDialog(null, "Seleccione alguna de las opciones porfavor.", "Boton Aplicar", JOptionPane.ERROR_MESSAGE);
            }
            
            if (cbxConfiguracionProd.isSelected()  && !cbxConfiguracionDin.isSelected())
            {
                //Actualizar la cantidad minima de productos.
                boolean resp = config.actualizarProdMin(cant_prod_nueva);
                
                if(!config.actualizarProdMin(cant_prod_nueva))
                {
                    txtConfiguracionProd.setEnabled(false);
                    cbxConfiguracionProd.setSelected(false);
                    JOptionPane.showMessageDialog(this, "Cantidad de producto actualizado correctamente.");
                }
            }
            
            if(cbxConfiguracionDin.isSelected() && !cbxConfiguracionProd.isSelected())
            {
                boolean resp2 = config.actualizarDineroMin(cant_din_nueva);
                
                if(!config.actualizarDineroMin(cant_din_nueva))
                {
                    txtConfiguracionDin.setEnabled(false);
                    cbxConfiguracionDin.setSelected(false);
                    JOptionPane.showMessageDialog(this, "Cantidad de dinero actualizado correctamente.");
                }
            }
            
            if(cbxConfiguracionDin.isSelected() && cbxConfiguracionProd.isSelected())
            {
                boolean resp = config.actualizarProdMin(cant_prod_nueva);
                boolean resp2 = config.actualizarDineroMin(cant_din_nueva);
                
                if (!config.actualizarProdMin(cant_prod_nueva) && !config.actualizarDineroMin(cant_din_nueva))
                {
                    txtConfiguracionProd.setEnabled(false);
                    cbxConfiguracionProd.setSelected(false);
                    txtConfiguracionDin.setEnabled(false);
                    cbxConfiguracionDin.setSelected(false);
                    JOptionPane.showMessageDialog(this, "Cantidades actualizadadas correctamente.");
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Los valores deben ser siempre positivos, arreglelos porfavor.", "Boton Actualizar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAplicarConfActionPerformed

