    private void txtConfiguracionProdKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtConfiguracionProdKeyTyped
        // Metodo para validar que en la cantidad se ingresen solo numeros.
        int k = (int) evt.getKeyChar();
        if (k >= 97 && k <= 122 || k >= 65 && k <= 90) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Solo puede ingresar números!!!", "Error Datos", JOptionPane.ERROR_MESSAGE);
        }

        int largo = txtConfiguracionProd.getText().trim().length();
        if (largo > 9) {
            evt.consume();
        }
    }//GEN-LAST:event_txtConfiguracionProdKeyTyped

