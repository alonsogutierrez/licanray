    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        // TODO add your handling code here:
        if (txtDinero.getText().trim().length() > 0) {
            RetiroDineroDao retiroDineroDao = new RetiroDineroDaoImpl();
            int usuarioActual = usuario.getIdUsuario();
            int dineroEnCaja = retiroDineroDao.dineroActual(usuarioActual);
            String verdad, aux = txtDinero.getText().trim();
            verdad = aux.replace(".", "");
            int dinero = Integer.parseInt(verdad);
            System.out.println("cant retiro dinero: " + dinero);

            if (dineroEnCaja < dinero) {
                JOptionPane.showMessageDialog(null, "El dinero que ingreso exede al dinero en caja, por favor intentelo una vez mas", "Retiro de Dinero", JOptionPane.ERROR_MESSAGE);
            } else {
                boolean resp = retiroDineroDao.retiroDinero(usuarioActual, dinero);
                System.out.println("respuesta bool: " +resp);

                if (resp == true) {
                    JOptionPane.showMessageDialog(null, "Retiro del dinero ingresado correctamente", "Retiro de Dinero", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al registrar el retiro del dinero en el sistema, intentelo nuevamente.", "Retiro de Dinero", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favo ingrese el dinero que desea retirar.", "Retiro de Dinero", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnRetirarActionPerformed

