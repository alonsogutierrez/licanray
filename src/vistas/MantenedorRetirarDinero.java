    private void txtDineroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDineroKeyReleased
        // TODO add your handling code here:
        DecimalFormat formateador = new DecimalFormat("###,###");
        String textoNuevo, texto = txtDinero.getText().trim();
        if (!texto.equals("")) {
            textoNuevo = texto.replace(".", "");

            String ff = formateador.format(Double.parseDouble(textoNuevo));

            txtDinero.setText(ff);
        }
    }//GEN-LAST:event_txtDineroKeyReleased

    private void txtDineroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDineroKeyTyped
        // TODO add your handling code here:
        int k = (int) evt.getKeyChar();
        if (k >= 97 && k <= 122 || k >= 65 && k <= 90) {

            evt.consume();
            JOptionPane.showMessageDialog(null, "¡No puede ingresar Letras!", "No puede ingresar letras", JOptionPane.ERROR_MESSAGE);
        }
        int largo = txtDinero.getText().trim().length();

        if (largo > 9) {
            evt.consume();
        }
    }//GEN-LAST:event_txtDineroKeyTyped

